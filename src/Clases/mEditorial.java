public class mEditorial {
    private int idEditorial;
    private String ediNombre;
    private String ediDireccion;
    private int LocalidadID;
    private String ediTelefono;
    private String ediEmail;

    public mEditorial() {
    }

    public int getIdEditorial() {
        return idEditorial;
    }

    public void setIdEditorial(int idEditorial) {
        this.idEditorial = idEditorial;
    }

    public String getEdiNombre() {
        return ediNombre;
    }

    public void setEdiNombre(String ediNombre) {
        this.ediNombre = ediNombre;
    }

    public String getEdiDireccion() {
        return ediDireccion;
    }

    public void setEdiDireccion(String ediDireccion) {
        this.ediDireccion = ediDireccion;
    }

    public int getLocalidadID() {
        return LocalidadID;
    }

    public void setLocalidadID(int LocalidadID) {
        this.LocalidadID = LocalidadID;
    }

    public String getEdiTelefono() {
        return ediTelefono;
    }

    public void setEdiTelefono(String ediTelefono) {
        this.ediTelefono = ediTelefono;
    }

    public String getEdiEmail() {
        return ediEmail;
    }

    public void setEdiEmail(String ediEmail) {
        this.ediEmail = ediEmail;
    }
    
    
    
}
