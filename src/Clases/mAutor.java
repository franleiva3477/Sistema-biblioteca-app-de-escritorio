package Clases;

import java.util.Date;


    
public class mAutor {
    private int idAutor;
    private String autNombre;
    private String autApellido;
    private Date autFechaNac;
    private String autBiografia;
    private Date autFecDes;

    public mAutor() {
    }

    public int getIdAutor() {
        return idAutor;
    }

    public void setIdAutor(int idAutor) {
        this.idAutor = idAutor;
    }

    public String getAutNombre() {
        return autNombre;
    }

    public void setAutNombre(String autNombre) {
        this.autNombre = autNombre;
    }

    public String getAutApellido() {
        return autApellido;
    }

    public void setAutApellido(String autApellido) {
        this.autApellido = autApellido;
    }

    public Date getAutFechaNac() {
        return autFechaNac;
    }

    public void setAutFechaNac(Date autFechaNac) {
        this.autFechaNac = autFechaNac;
    }

    public String getAutBiografia() {
        return autBiografia;
    }

    public void setAutBiografia(String autBiografia) {
        this.autBiografia = autBiografia;
    }

    public Date getAutFecDes() {
        return autFecDes;
    }

    public void setAutFecDes(Date autFecDes) {
        this.autFecDes = autFecDes;
    }
    
    
    
    

}
